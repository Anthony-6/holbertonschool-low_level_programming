#include "3-calc.h"

/**
 * op_add - additionnate two numer
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substract two numer
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplicate two numer
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide two numer
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulate two numer
 * @a: first number
 * @b: second number
 * Return: sum
 */
int op_mod(int a, int b)
{
	return (a % b);
}
